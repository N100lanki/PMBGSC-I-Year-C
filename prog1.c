// program to take value from user

#include<stdio.h>
void main()
{
	int a;
	float b;
	
	printf("Enter value of a : ");
	scanf("%d",&a);
	
	printf("Enter value of b : ");
	scanf("%f",&b);
	
	printf("\nvalue of a : %d\n",a);	
	printf("\nvalue of b : %f\n",b);	
	printf("\nvalue of b : %.3f\n",b);	

}
